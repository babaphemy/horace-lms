import { toast } from 'react-toastify';

const { error, success, warn, info } = toast;

export const notifyError = (message: string) => {
	error(message, {
		position: 'top-right',
		autoClose: 3000,
		hideProgressBar: false,
		closeOnClick: true,
		pauseOnHover: true,
		draggable: true,
		progress: undefined,
	});
};

